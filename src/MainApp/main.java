    private void txtSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyPressed
        // TODO add your handling code here
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            try {
                String sql = "select * from employee_list where srno = ?";
                ps = conn.prepareStatement(sql);
                
                ps.setString(1, txtSearch.getText());
                st = ps.executeQuery();
                if (st.next()) {
                    txtFname1.setText(st.getString(2));
                    txtLname1.setText(st.getString(3));
                    txtDepartment1.setText(st.getString(4));
                    txtDesignation1.setText(st.getString(5));
                    txtNumber1.setText(st.getString(6));
                    if (st.getString(7).equals("Active")) {
                        r_Active1.setSelected(true);
                        r_Inactive1.setSelected(false);
                    } else if (st.getString(7).equals("In-Active")) {
                        r_Active1.setSelected(false);
                        r_Inactive1.setSelected(true);
                    }
                    txtAddress1.setText(st.getString(8));
                            
                }
                        
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_txtSearchKeyPressed

