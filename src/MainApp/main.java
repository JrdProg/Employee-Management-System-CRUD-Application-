    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Are you sure to Add this record ?","Add Record", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                String sql = "INSERT INTO `employee_list`(`fname`, `lname`, `department`, `designation`, `number`, `status`, `address`, `salary`) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
                ps = conn.prepareStatement(sql);
                
                ps.setString(1, txtFname.getText());
                ps.setString(2, txtLname.getText());
                ps.setString(3, txtDepartment.getSelectedItem().toString());
                ps.setString(4, txtDesignation.getSelectedItem().toString());
                ps.setString(5, txtNumber.getText());
                ps.setString(6, status);
                ps.setString(7, txtAddress.getText());
                ps.setString(8, txtSalary.getText());
                
                ps.execute();
                JOptionPane.showMessageDialog(null, "Data Saved Successfully");
                 reset();      
                        
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
           
        } else {
            JOptionPane.showMessageDialog(null, "Data not Successfully saved in Database", "Failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addbtnActionPerformed

